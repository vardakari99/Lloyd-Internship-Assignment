window.onload = function() {
    var countryIn = document.getElementById("country"),
    stateIn = document.getElementById("state"),
    cityIn = document.getElementById("city");

    for (var country in stateObject){
        countryIn.options[countryIn.options.length] = new Option(country, country);
    }

    countryIn.onchange = function(){
        stateIn.length = 1; //remove all options bar first
        cityIn.length = 1; //remove all options bar first
        var countrySelected = this.value; 
        if(this.selectedIndex < 1) return; //done
        for (var state in stateObject[countrySelected]){
            stateIn.options[stateIn.options.length] = new Option(state, state);
        }
    }
    

    countryIn.onchange(); //reset in case page is reloaded

    stateIn.onchange = function(){
    cityIn.length = 1; //remove all options bar first
    
    var stateSelected = this.value;
    if(this.selectedIndex < 1) return; //done

    var city = stateObject[countryIn.value][stateSelected];
    for (var i = 0; i < city.length; i++){
        cityIn.options[cityIn.options.length] = new Option (city[i],city[i]);
    }
    }

    cityIn.onchange = function(){
        var citySelected = this.value;
        document.getElementById("dcity").innerHTML = citySelected;
    }

}

